package com.rt;

public class AreaShapes {
	public static void main(String[] args) {
		
		int Area=3;
		
		switch(Area) {
		
		case 1:{
			System.out.println("Square");
			break;
			
		}
		case 2:{
			System.out.println("Circle");
			break;
			
		}
		case 3:{
			System.out.println("Rectangular");
			break;
			
		}
		
		default :{
			System.out.println("Invalid input");
		}
		}
	}
}



